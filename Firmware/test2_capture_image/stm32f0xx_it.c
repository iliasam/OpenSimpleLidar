//connected to ADC
void DMA1_Channel1_IRQHandler(void)
{  
  // DMA1 Channel1 transfer COMPLETE interrupt 
  if(DMA_GetITStatus(DMA1_IT_TC1))
  {
    TIM1->CR1 &= (uint16_t)(~((uint16_t)TIM_CR1_CEN));//stop TIM1 - stop PWM pulses
    TIM_SetCounter(TIM1,0);
    DMA_Cmd(DMA1_Channel1, DISABLE);
    capture_done_flag = 1;
  }//end if
  
  //DMA1 Channel1 transfer ERROR interrupt 
  if(DMA_GetITStatus(DMA1_IT_TE1)){DMA_ClearITPendingBit(DMA1_IT_TE1);}
  
  DMA_ClearITPendingBit(DMA1_IT_GL1);
}


